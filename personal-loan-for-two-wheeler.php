    var base_url = "https://gateway.credmudra.com/apis/";
    $(".sendOtp").on("click", function() {
        const mobileNo = $("#mobile").val();
        if (mobileNo.length === 10 && $("#chkterms").is(":checked")) {
            webengage.user.setAttribute(
                "we_phone",
                "+91" + $("#phone-number").val()
            );
            $(".loader").css({
                display: "block"
            });
            var settings = {
                url: base_url + "public/send-otp",
                method: "POST",
                timeout: 0,
                headers: {
                    "Content-Type": "application/json",
                },
                data: JSON.stringify({
                    data: {
                        contactNo: mobileNo,
                        resend: false,
                    },
                }),
            };

            $.ajax(settings)
                .done(function(response) {
                    $(".mobileNo").html(mobileNo);
                    sessionStorage.setItem("userPhone", mobileNo);
                    $("#snackbar").html("OTP sent successfully");
                    $("form[name='mobile_form']").trigger("submit");

                    setTimeout(function() {
                        $(".loader").css({
                            display: "none"
                        });
                    }, 5000);

                    var x = document.getElementById("snackbar");
                    x.className = "show";
                    setTimeout(function() {
                        x.className = x.className.replace("show", "");
                    }, 3000);
                })
                .fail(function(jqXHR, textStatus) {
                    $("#snackbar").html("Something went wrong");
                    var x = document.getElementById("snackbar");
                    x.className = "show";
                    setTimeout(function() {
                        x.className = x.className.replace("show", "");
                    }, 3000);
                });
        } else {
            $("#snackbar").html("Something went wrong");
            var x = document.getElementById("snackbar");
            x.className = "show";
            setTimeout(function() {
                x.className = x.className.replace("show", "");
            }, 3000);
            return false;
            $(".loader").css({
                display: "none"
            });
        }
    });
